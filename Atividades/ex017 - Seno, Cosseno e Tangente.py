import math
ang = float(input("Digite o ângulo que você desja: "))
print("O ângulo de {:.2f}º tem o seno de {:.2f}".format(ang, math.sin(math.radians(ang))))
print("O ângulo de {:.2f}º tem o seno de {:.2f}".format(ang, math.cos(math.radians(ang))))
print("O ângulo de {:.2f}º tem o seno de {:.2f}".format(ang, math.tan(math.radians(ang))))